<li>pinia 可以直接修改 state 中的数据，而 vuex 需要通过 mutations 来修改 state 中的数据。</li>
<li>pinia 的模块化不需要 modules，而 vuex 的模块化需要 modules。</li>
<li>pinia 的热模块替换功能比 vuex 更好。</li>
<li>pinia 的插件和社区支持比 vuex 更好。</li>
<li>pinia 的 TypeScript 支持比 vuex 更好。</li>
<li>pinia 的 devtools 支持比 vuex 更好。</li>
</ol>
